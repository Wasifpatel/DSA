public class _1Bitwise_operator {
    public static void main(String[] args) {
        System.out.println(5&6);
        System.out.println(5|6);
        System.out.println(5^6);
        System.out.println(~5);
        System.out.println(5<<2);
        // a<<b = a*2^b
        System.out.println(5>>2);
        // a>>b = a/2^b
    }
}
